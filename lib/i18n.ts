// Translation dictionaries. Every user-visible string lives here so the whole
// experience (citizen + caseworker) can be rendered in EN / ES / AR.
// Arabic also drives right-to-left (RTL) layout.
// ---------------------------------------------------------------------------

export const LOCALES: { code: Locale; label: string; native: string; dir: "ltr" | "rtl" }[] = [
  { code: "en", label: "English", native: "English", dir: "ltr" },
  { code: "es", label: "Spanish", native: "Español", dir: "ltr" },
  { code: "ar", label: "Arabic", native: "العربية", dir: "rtl" },
]

export function dirFor(locale: Locale): "ltr" | "rtl" {
  return LOCALES.find((l) => l.code === locale)?.dir ?? "ltr"
}

